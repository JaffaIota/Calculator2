namespace ClassLibrary1
{
    public class Calculator
    {
        public int AddTwoIntegers(int a, int b)
        {
            int c = a + b;
            return c;
        }

        public int Subtract(int a, int b)
        {
            int c = a - b;
            return c;
        }

        public int Multiplied(int a, int b)
        {
            int c = a * b;
            return c;
        }
        public int Divide(int a, int b)
        {
            int c = a / b;
            return c;
        }

    }
}