namespace ClassLibrary1
{
    public class Calculator
    {
        public int AddTwoIntegers(int a, int b)
        {
            int c = a + b;
            return c;
        }

        public int SubTwoIntegers(int a, int b)
        {
            int c = a - b;
            return c;
        }

        public int MultTwoIntegers(int a, int b)
        {
            int c = a * b;
            return c;
        }

        public int DivTwoIntegers(int a, int b)
        {
            int c = a / b;
            return c;
        }
    }
}